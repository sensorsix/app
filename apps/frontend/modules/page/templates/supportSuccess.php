<h2>Roadmap</h2>
<hr>
<a id="4_1"></a>
<h3>How do I create a new road map?</h3>
<ol>
<li> Go to the Roadmap module<br>
<img src="/images/support/roadmap1.png" style="width:50%;" class="helpimg">
<li> Click Add roadmap in the top right
<li> Choose a name for the road map
<li> Select the projects you wish to display in the road map
</ol>
<p>Now the roadmap will display the information in the projects you have chosen. You can click the road map to view it or click edit to edit details about purpose and status. If changes are made to the project they will instantly be displayed in the roadmap.</p>
<hr>
<a id="4_2"></a>
<h3>How do I choose what the roadmap should display? </h3>
<ol>
  
<li> Go to the roadmap module
<li> When the mouse is above the title of a road map an edit button appears. Click this button<br>
<img src="/images/support/roadmap2.png" style="width:50%;" class="helpimg">

<li> The different options allow you to control the level of detail you wish to display to viewers of the road map
<li> There are to options to display the road map. Either as a gantt like timeline or as a list
<li>  Choose the description sub menu and enter a text that describes the purpose or background of the roadmap
<li> Choose the Project menu in order to choose which projects to display in the road map
<li> Click Save changes

</ol>
<hr>
<a id="4_3"></a><h3>
How do I share a roadmap? </h3>
<ol>
 <li> Go to the roadmap module
<li> On the list of roadmaps there is a link that you can copy and send to the people you wish to share it with
</ol>

