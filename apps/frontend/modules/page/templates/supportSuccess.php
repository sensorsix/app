<h2>Collaboration</h2>
<p>You can easily engage internal and external stakeholders by going to the collaborate section. This will allow you to get input on new ideas, weighing the different criteria and rating each item on these criteria.</p>
<img src="/images/support/collaboration.png" style="width:50%;" class="helpimg">
<hr>
<a id="3_1"></a>
<h3>How do I create a new workspace to engage collaborators?</h3>

<ol>
<li> Click add new<br>
<img src="/images/support/collaboration1.png" style="width:50%;" class="helpimg"><br>
if you want them to rate Items on one or more of the available criteria, select Rate items. If you want the to weigh the importance of the different criteria, Select Prioritise criteria and if you want them to be able to submit ideas for your product, select Gather ideas
<li> When you are ready to collaborate turn the workspace on in the top right
<li> Click create

</ol>
<hr>
<a id="3_2"></a>
<h3>How do I invite collaborators to score items on a particular criteria?</h3>
<ol>
<li> Go to the collaborate menu
<li> Select the workspace you want to work on<br>
<img src="/images/support/collaboration2.png" style="width:50%;" class="helpimg">
<li> Click the Rate Items button on
<li> Select or deselect columns, rows or individual cells <br>
<img src="/images/support/collaboration3.png" style="width:50%;" class="helpimg">

</ol>
<hr>
<a id="3_3"></a>
<h3>How do I Invite collaborators to weigh different criteria?</h3>
<ol>
<li> Go to the collaborate menu
<li> Select the workspace you want to work on
<li> Click the prioritise criteria  button on
<li> Choose the method you wish to use for weighing the criteria
</ol>
<hr>
<a id="3_4"></a>
<h3>How do I invite collaborators to submit ideas to the product?</h3>
<ol>
<li>Go to the collaborate menu
<li>Select the workspace you want to work on
<li>Click the Gather ideas button on
<li>Choose whether you want collaborators to be able to submit ideas, see other ideas or allow voting on other items. 
</ol>
Items will not appear to other collaborators before they have been approved by the administrator.

<hr>
<a id="3_5"></a>
<h3>How do I share a workspace?</h3>
<ol>
<li> Go to the collaborate menu
<li> Find the workspace you want to share
<li> Either click the copy button for quick sharing of the link to the workspace
<li> Or open the workspace and click the share sub menu. Here you can choose to share it on social media, through a link or get a code to embed the workspace on a website<br>
<img src="/images/support/collaboration4.png" style="width:50%;" class="helpimg">
</ol>
