<h2>Settings</h2>
<hr>
<a id="5_1"></a><h3>
How do I change Password</h3>

<ol>
<li> Select the My account menu point in the top right drop down where your user name is displayed
<li> Choose Change password
<li> Fill out the new password
<li> Click save
</ol>

<hr>
<a id="5_2"></a><h3>
How do I invite team members as editors?</h3>
<ol>
<li> Select the My account menu point in the top right drop down where your user name is displayed
<li> Choose Members submenu
<li> Click add user and fill in the email address 
<li> Click save

</ol>
<hr>
<a id="5_3"></a><h3>
How do I customise the workspaces for my account?</h3>
<ol>
<li>Select the My account menu point in the top right drop down where your user name is displayed
<li>Choose Design<br>
<img src="/images/support/settings1.png" style="width:50%;" class="helpimg">
<li>Choose the colour of the top bar by selecting colour 
<li>Upload company logo if necessary
<li>If you want to direct collaborators to your web site when they click the logo just write the URL in the Logo URL field
<li>Click save and all workspaces in your account will now display your customized design
</ol>
<hr>
<a id="5_4"></a><h3>
How do I select templates for creating projects? </h3>
<ol>
<li>Select the My account menu point in the top right drop down where your user name is displayed
<li>Choose Template editor<br>
<img src="/images/support/settings2.png" style="width:50%;" class="helpimg">
<li>Give the template a name,
<li>Select which decision type it applies to
<li>Add the default criteria that should be available. Give them a name and choose type and measurement function

</ol>
<p>Now this template will be available from a drop down when you create new projects
</p>
<hr>
<a id="5_5"></a><h3>
How do I use the Sensor Six API? </h3>
<ol>
<li> Select the My account menu point in the top right drop down where your user name is displayed
<li> Select the API sub menu for details<br>
<img src="/images/support/settings3.png" style="width:50%;" class="helpimg">
<li>Copy the access token to use for API calls
</ol>
<hr>
<a id="5_6"></a><h3>
How do I change concepts such as project, release and folder in the user interface?</h3>
<ol>
<li> Select the My account menu point in the top right drop down where your user name is displayed
<li> Select the labels sub menu<br>
<img src="/images/support/settings4.png" style="width:50%;" class="helpimg">
<li> Adjust the wording according to the terminology you wish to use

<p>Now the concepts will have changed in the user interface

</ol>
