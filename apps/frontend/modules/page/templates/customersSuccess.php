<h3>Challenge</h3>
<img src="images/logo-coop.png" alt="Coop" class="pull-right" style="padding:50px;" />
<p>Coop is Denmark’s largest retailer and has 40.000 employees. Making it possible for them to access enterprise applications on their own devices is a good way of optimizing use of IT resources. But the employees are a very diverse group and it is difficult to know precisely what the different employee groups need to do and what devices they would like to use.
</p>
<h3>Solution</h3>
<p>Sensor Six made it possible to get detailed input from these diverse stakeholder groups about what use cases were important to them and what devices they used. This could be broken down by stakeholder group to drill down and see exactly what the needs were in different parts of the business. This information allowed Coop to plan a road map and it allowed them to chose the right BYOD solution and implement the right features to optimally support the needs of the workforce. Ultimately resulting in a cheaper implementation since only 60% of the proposed features were necessary.
</p>

